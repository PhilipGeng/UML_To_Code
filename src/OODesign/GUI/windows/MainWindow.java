package OODesign.GUI.windows;

import javax.swing.*;
/**
 * This part is used to create a general JFrame
 * @author ZHANG Beibei
 * @version 1.0
 */

public class MainWindow extends JFrame{
	public static final long serialVersionUID=138938122;
	/**
	 * This constructor set the general parameters of a JFrame
	 * 
	 * @param width
	 * @param height
	 * @param locationX
	 * @param locationY
	 * @param title
	 */
    public MainWindow(int width, int height, int locationX, int locationY, String title){
    	super(title);
    	this.setSize(width, height);
    	this.setLocation(locationX, locationY);
    	this.setVisible(true);
    }
    /**
     * this is an null constructor
     */
    
    public MainWindow(){
    	
    }
}
